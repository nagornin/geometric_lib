def area(a, b):
    '''
    Returns area of a rectangle with sides a and b

    Example usage:
    > area(4, 3)
    12
    '''
    return a * b

def perimeter(a, b):
    '''
    Returns perimiter of a rectangle with sides a and b

    Example usage:
    > perimiter(7, 4)
    22
    '''
    return (a + b) * 2
