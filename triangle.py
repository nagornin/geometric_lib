def area(a, h):
    '''
    Returns area of a triangle with base a and height h

    Example usage:
    > area(4, 5)
    10
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
    Returns perimiter of a triangle given sides a, b, c

    Example usage:
    > perimiter(1, 2, 3)
    6
    '''
    return a + b + c
